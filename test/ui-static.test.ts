import assert from "node:assert/strict";
import { readFileSync } from "node:fs";
import test from "node:test";

test("React UI keeps task-first Sources wording and accessible icon controls", () => {
  const app = readFileSync("src/web/src/App.tsx", "utf8");
  const client = readFileSync("src/web/src/api/client.ts", "utf8");
  const ui = readFileSync("src/web/src/components/ui.tsx", "utf8");
  const css = readFileSync("src/web/src/styles/globals.css", "utf8");

  assert.match(app, /type View = "todos" \| "sources" \| "settings"/);
  assert.match(app, />To-Do</);
  assert.match(app, />Sources</);
  assert.doesNotMatch(app, />Evidence</);
  assert.match(app, /label="Refresh"/);
  assert.match(app, /aria-label="Organize all recent sessions"/);
  assert.match(app, /aria-label="Organize empty inbox"/);
  assert.match(app, /`Open \$\{children\}`/);
  assert.match(app, /aria-label=\{`Open sources for \$\{todo.title\}`\}/);
  assert.match(ui, /aria-label=\{label\}/);
  assert.match(app, /Agent:<\/span>/);
  assert.match(app, /originLabel\(todo\)/);
  assert.match(app, /Temporary session/);
  assert.match(app, /sourceFilter/);
  assert.match(app, /Source filter/);
  assert.match(app, /Load more/);
  assert.match(app, /max-h-\[calc\(100vh-220px\)\]/);
  assert.match(app, /ensureSessionLoaded/);
  assert.match(app, /scanChangedSources/);
  assert.match(app, /Source scan finished/);
  assert.match(app, /Source scan failed/);
  assert.match(app, /startupStatusMessage/);
  assert.match(app, /mergeSessions/);
  assert.match(app, /sessionId/);
  assert.match(app, /scrollIntoView/);
  assert.match(app, /todoGroups\(props\.openTodos\)/);
  assert.match(app, /OPEN_GROUP_PREVIEW_LIMIT/);
  assert.match(app, /Show \{hiddenCount\} more/);
  assert.match(app, /aria-expanded=\{expanded\}/);
  assert.match(app, /originProjectLabel\(todo\)/);
  assert.match(app, /originSessionLabel\(todo\)/);
  assert.match(app, /Completed \/ ignored/);
  assert.match(app, /max=\{200\}/);
  assert.match(app, /organizeStatus\(result\)/);
  assert.match(client, /Extraction needs setup/);
  assert.match(client, /No actionable cards found in some sessions/);
  assert.match(client, /Some sessions could not be processed/);
  assert.match(client, /No Codex sessions were found in the source path/);
  assert.doesNotMatch(app, />LLM unavailable</);
  assert.match(css, /overflow-x: hidden/);
});
